import { createContext, useState, useEffect } from "react"
const API = "http://localhost:4000/api"
const AuthContext = createContext()

export const AuthProvider = ({ children }) => {
  const [user, setUser] = useState(null)
  const [authCookie, setAuthCookie] = useState(null)
  const [isLoading, setIsLoading] = useState(true)

  const Login = async (email, password) => {
    try {
      console.log("üîê === AUTH CONTEXT LOGIN ===")
      console.log("üìß Email:", email)
      
      const response = await fetch(`${API}/login`, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ email, password }),
        credentials: "include" // Para incluir cookies en la peticion
      })
      console.log("üì° Login response status:", response.status)

      const data = await response.json()
      console.log("üì¶ Login response data:", data)

      if (!response.ok) {
        console.log("‚ùå Login failed:", data.message)
        throw new Error(data.message || "Error en la autenticaci√≥n")
      }
      console.log("‚úÖ Login successful")
      // NECESITAMOS OBTENER EL USERTYPE DEL TOKEN O DEL SERVIDOR
      // Hacer una peticion adicional para obtener la info del usuario
      const userInfoResponse = await fetch(`${API}/validateAuthToken`, {
        method: "POST",
        credentials: "include",
        headers: { 'Content-Type': 'application/json' }
      })
      if (userInfoResponse.ok) {
        const userInfo = await userInfoResponse.json()
        console.log("üë§ User info from server:", userInfo)
        
        const userData = { 
          email, 
          userType: userInfo.userType,
          userId: userInfo.userId
        }
        
        localStorage.setItem("user", JSON.stringify(userData))
        setUser(userData)
        setAuthCookie(true) // Indicador de que hay cookie valida
        
        return { success: true, message: data.message }
      } else {
        throw new Error("No se pudo obtener informaci√≥n del usuario")
      }
    } catch (error) {
      console.log("‚ùå Login error:", error.message)
      return { success: false, message: error.message }
    }
  }
  const GoogleLogin = async (userData) => {
    try {
      console.log("üîê === GOOGLE AUTH CONTEXT LOGIN ===")
      console.log("üì¶ Datos recibidos:", userData)
      
      const userInfo = {
        email: userData.email,
        name: userData.name,
        lastName: userData.lastName,
        userType: userData.userType,
        isVerified: userData.isVerified,
        id: userData.id
      }
      localStorage.setItem("user", JSON.stringify(userInfo))
      setUser(userInfo)
      setAuthCookie(true)
      // Solo un toast aqui
      console.log("‚úÖ Usuario autenticado correctamente con Google")

      return { success: true, message: "Autenticaci√≥n con Google exitosa" }
    } catch (error) {
      console.log("‚ùå Google login error:", error.message)
      return { success: false, message: error.message }
    }
  }
  const logout = async () => {
    try {
      console.log("üö™ === LOGOUT ===")
      // Llamar al endpoint de logout en el backend para limpiar la cookie
      await fetch(`${API}/logout`, {
        method: "POST",
        credentials: "include", // Para incluir cookies en la petici√≥n
      })
      console.log("‚úÖ Server logout successful")
    } catch (error) {
      console.error("‚ùå Error durante el logout:", error)
    } finally {
      // Limpiar datos locales independientemente de si la petici√≥n al servidor tuvo √©xito
      console.log("üßπ Cleaning local data")
      localStorage.removeItem("user")
      setAuthCookie(null)
      setUser(null)
    }
  }
  // Verificar autenticacion al cargar la aplicaci√≥n (SOLO UNA VEZ)
  useEffect(() => {
    const checkAuth = async () => {
      try {
        console.log("üîç === CHECKING INITIAL AUTH ===") 
        // Intentar restaurar usuario desde localStorage
        const savedUser = localStorage.getItem("user")
        console.log("üíæ Saved user in localStorage:", savedUser)
        if (savedUser) {
          console.log("üîÑ Found saved user, validating with server...")
          // Verificar si la sesi√≥n sigue siendo v√°lida con el servidor
          const response = await fetch(`${API}/validateAuthToken`, {
            method: "POST",
            credentials: "include",
            headers: {
              'Content-Type': 'application/json'
            }
          })
          console.log("üì° Validation response status:", response.status)
          if (response.ok) {
            console.log("‚úÖ Session valid - restoring user")
            // Sesi√≥n v√°lida, restaurar usuario
            setUser(JSON.parse(savedUser))
            setAuthCookie(true) // Indicador de que hay cookie v√°lida
          } else {
            console.log("‚ùå Session invalid - cleaning local data")
            // Sesi√≥n inv√°lida, limpiar datos locales
            localStorage.removeItem("user")
            setUser(null)
            setAuthCookie(null)
          }
        } else {
          console.log("üì≠ No saved user found")
        }
      } catch (error) {
        console.error("‚ùå Error checking auth:", error)
        // En caso de error, limpiar datos locales
        localStorage.removeItem("user")
        setUser(null)
        setAuthCookie(null)
      } finally {
        console.log("‚úÖ Initial auth check complete")
        setIsLoading(false)
      }
    }
    checkAuth()
  }, []) // Array vacio - solo ejecutar una vez al montar
  return (
    <AuthContext.Provider value={{ user, Login, GoogleLogin, logout, authCookie, setAuthCookie, API, isLoading }}>
      {children}
    </AuthContext.Provider>
  )
}
// Export el contexto para poder usarlo en el hook
export { AuthContext }